package entidades;

/**
 *
 * @author froi-pc
 */
public class CitaLaboratorio extends Entidad{
    
    private String codigo;
    private String fecha;
    private String hora;
    private double costo;
    private String codigoMedico;
    private String dpiPaciente;
    private String codigoExamen;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public double getCosto() {
        return costo;
    }

    public void setCosto(double costo) {
        this.costo = costo;
    }

    public String getCodigoMedico() {
        return codigoMedico;
    }

    public void setCodigoMedico(String codigoMedico) {
        this.codigoMedico = codigoMedico;
    }

    public String getDpiPaciente() {
        return dpiPaciente;
    }

    public void setDpiPaciente(String dpiPaciente) {
        this.dpiPaciente = dpiPaciente;
    }

    public String getCodigoExamen() {
        return codigoExamen;
    }

    public void setCodigoExamen(String codigoExamen) {
        this.codigoExamen = codigoExamen;
    }
    
}
